def clean_text(t):
    t = re.sub(r"<br\s*/?>", " ", str(t), flags=re.IGNORECASE)
    t = re.sub(r"\s+", " ", t).strip()
    return t

def normalize_label(v):
    v = str(v).strip().lower()
    if v in {"pos", "positive", "1", "true"}: return "positive"
    if v in {"neg", "negative", "0", "false"}: return "negative"
